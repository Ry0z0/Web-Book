package entity;

/**
 *
 * @author Hi Ryo
 */
public class Cart {
    int id;
    int amount;
    String image;
    double price;
    String name;

    public Cart(int id, int amount, String image, double price, String name) {
        this.id = id;
        this.amount = amount;
        this.image = image;
        this.price = price;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
